#Functions that you can use of Lists - len, min, max, sum, sort

#Creating a list
num_list = [1,2,4,100,200,5,5,5,6]

print("I am printing the list" ,num_list)

#Methods are diffirent from functions on a list
num_list.append(45)

print("I am printing the list after append" ,num_list)

#Functions that we can use of a list
print("The length of this list is ",len(num_list))

print("The min number in the list is ",min(num_list))
print("The max in the list is ", max(num_list))
print("The sum of the list is ", sum(num_list))

print("Lets sort this list", sorted(num_list))





